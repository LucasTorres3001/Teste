package classes;


public class Accessory {
    
    public static double Vo(int f, double g){
        return f * g;
    }
    
    public static double S(int d, double e){
        return d * e;
    }
    
    public static double V(double a, double b, double c){
        return Math.sqrt((a*a) + (2*b*c));
    }
    
    public static double Massa(int h, double i){
        return h * i;
    }
    
    public static double Q(double j, double k){
        return j * k;
    }
    
    public static double Impuso(double o, double p){
        return o * p;
    }
    
    public static double Forca(double q, double r){
        return q * r;
    }
    public static double Ecinetica(double u, double w){
        return (u * (w * w)) / 2;
    }
}
